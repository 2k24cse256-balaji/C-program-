#include <stdio.h>

int main() {

    int a, b;

    // Read inputs

    scanf("%d", &a);

    scanf("%d", &b);

    // Perform operations

    printf("Sum: %d\n", a + b);

    printf("Difference: %d\n", a - b);

    printf("Product: %d\n", a * b);

    printf("Quotient: %d\n", a / b);

    printf("Remainder: %d\n", a % b);

    return 0;

}
